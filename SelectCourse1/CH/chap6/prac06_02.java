package SelectCourse1.CH.chap6;

import java.util.Scanner;

public class prac06_02 {
    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        int n = scan.nextInt();
        for (int i = 1; i <= n; i++) {
            System.out.println(i * 2);
        }
    }
}
